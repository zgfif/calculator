export const devision = (a, b) => {
  return b != 0 ? a / b : 'Cannot divide by zero';
};

export const multiply = (a, b) => a * b;

export const subt = (a, b) => a - b;

export const summ = (a, b) => a + b;

export const percent = (a) => a / 100;

export const changeSign = (a) => -a;
