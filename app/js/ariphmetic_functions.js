// / * - + % change_sign

export function devision(a, b) {
    if (b != 0) {
        return a / b
    } else {
        return 'Cannot divide by zero'
    }
};

export function multiply(a, b) {
    return a * b
};

export function subm(a, b) {
    return a - b
};

export function summ(a, b) {
    return a + b
};

export function percent(a) {
    return a / 100;
};

export function changeSign(a) {
    return -a;
};
